public class OrdercostInfo {

	private String  facilityName;//设施名称
	private Double unitPrie_i;//单价
	private Integer number;//数量
	private String  unit;//单位
	private Double  totalprice = new Double(0);//总价
	private String  type;//收费类型
 
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getFacilityName() {
		return facilityName;
	}
	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}
	 
	public Double getUnitPrie_i() {
		return unitPrie_i;
	}
	public void setUnitPrie_i(Double unitPrieI) {
		unitPrie_i = unitPrieI;
	}
	public Integer getNumber() {
		return number;
	}
	public void setNumber(Integer number) {
		this.number = number;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Double getTotalprice() {
		return totalprice;
	}
	public void setTotalprice(Double totalprice) {
		this.totalprice = totalprice;
	}
	@Override
	public String toString() {
		return "OrdercostInfo [facilityName=" + facilityName + ", number="
				+ number + ", totalprice=" + totalprice + ", unit=" + unit
				+ ", unitPrie_i=" + unitPrie_i + "]";
	}
	
	
}
